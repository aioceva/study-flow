import { Adaptation } from "@/types";

export function quizPrompt(adaptation: Adaptation, today: string): string {
  return `Въз основа на следната адаптация на урок, генерирай 20 въпроса за quiz.

АДАПТАЦИЯ:
${JSON.stringify(adaptation, null, 2)}


ПРАВИЛА:

Съдържание

Използвай знанието от адаптацията.

Не добавяй нови факти или нови учебни термини.

Структура

Създай точно 20 въпроса.

Направи по 3 въпроса от всеки модул, общо 12.

Добави 8 общи въпроса за целия урок.

Не задавай въпроси, които са почти еднакви, но с различни думи.

Типове въпроси

Използвай три типа въпроси:

Разбиране – проверява дали детето разбира идеята.

Разграничаване – проверява дали детето различава близки понятия.

Прилагане – поставя знанието в проста ситуация.

Разпределение:

около 40% разбиране

около 30% разграничаване

около 30% прилагане

Поне 6 въпроса трябва да са с проста ситуация.

Фактология

Не повече от 5 въпроса да са за числа, години или брой.

Питай за число или година само ако това е важно за разбирането на урока.

Качество на въпроса

Въпросът е кратък и ясен.

Използвай прост език.

Един въпрос проверява едно знание.

Не използвай подвеждащи въпроси.

Не използвай двойно отрицание.

Не задавай въпрос с повече от един верен отговор.

Отговори

Всеки въпрос има точно 3 отговора: "a", "b", "c".

Има точно един верен отговор.

Не използвай “всички изброени” или “няма верен отговор”.

Explanation

За всеки въпрос добави поле "explanation".

Explanation е 1 кратко изречение.

Обяснява защо верният отговор е верен.


Върни САМО валиден JSON без никакъв друг текст:

{
  "meta": {
    "user": "${adaptation.meta.user}",
    "subject": "${adaptation.meta.subject}",
    "lesson": ${adaptation.meta.lesson},
    "generated": "${today}",
    "total": 20
  },
  "questions": [
    {
      "id": 1,
      "module_id": 1,
      "card_id": 1,
      "question": "Въпросът",
      "options": [
        { "id": "a", "text": "Отговор А", "correct": true },
        { "id": "b", "text": "Отговор Б", "correct": false },
        { "id": "c", "text": "Отговор В", "correct": false }
      ],
      "explanation": "Кратко обяснение защо отговор А е верен."
    }
  ]
}

Генерирай точно 20 въпроса: 3 за модул 1 (module_id:1), 3 за модул 2 (module_id:2), 3 за модул 3 (module_id:3), 3 за модул 4 (module_id:4), 8 общи (module_id:0).`;
}
