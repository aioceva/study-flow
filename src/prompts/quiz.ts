import { Adaptation } from "@/types";

export function quizPrompt(adaptation: Adaptation, today: string): string {
  return `Въз основа на следната адаптация на урок, генерирай 20 въпроса за quiz.

АДАПТАЦИЯ:
${JSON.stringify(adaptation, null, 2)}

ПРАВИЛА:

Съдържание
Използвай термините и фактите от адаптацията.
Не добавяй нови факти или нови учебни термини.
Използвай кратки изречения и ясен и прост език.

Структура
Създай точно 20 въпроса.
За всеки модул създай по 3 въпроса:
един въпрос за разбиране (проверява дали детето разбира).  
един въпрос за разграничаване (проверява дали детето различава близки понятия, като дава близки отговори)
един въпрос за прилагане (поставя знанието в проста реална ситуация)
Добави 8 въпроса за целия урок.
Всеки въпрос има точно 3 отговора: "a", "b", "c".
Има точно един верен отговор.

Примери за типове въпроси
Знание:
Растенията имат нужда от вода, за да растат.
Въпрос за разбиране:
Защо растенията имат нужда от вода?
Въпрос за разграничаване:
Кое от изброените има нужда от вода, за да расте?
Въпрос за прилагане:
Ако не поливаш едно растение, какво ще се случи с него?

Explanation
За всеки въпрос добави поле "explanation".
Explanation е кратко изречение, което обяснява защо верният отговор е верен.

Върни САМО валиден JSON без никакъв друг текст:

{
  "meta": {
    "user": "${adaptation.meta.user}",
    "subject": "${adaptation.meta.subject}",
    "lesson": ${adaptation.meta.lesson},
    "generated": "${today}",
    "total": 20
  },
  "questions": [
    {
      "id": 1,
      "module_id": 1,
      "card_id": 1,
      "question": "Въпросът",
      "options": [
        { "id": "a", "text": "Отговор А", "correct": true },
        { "id": "b", "text": "Отговор Б", "correct": false },
        { "id": "c", "text": "Отговор В", "correct": false }
      ],
      "explanation": "Кратко обяснение защо отговор А е верен."
    }
  ]
}

Генерирай точно 20 въпроса: 3 за модул 1 (module_id:1), 3 за модул 2 (module_id:2), 3 за модул 3 (module_id:3), 3 за модул 4 (module_id:4), 8 общи (module_id:0).`;
}
